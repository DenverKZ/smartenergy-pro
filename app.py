# ============================================================================
# ПОДКЛЮЧЕНИЕ БИБЛИОТЕК И МОДУЛЕЙ
# ============================================================================

import streamlit as st
# streamlit - библиотека для создания веб-приложений на Python
# st - это сокращение, стандартное название в сообществе Streamlit

from utils.theme import init_theme, get_theme_colors, get_css, theme_selector
# init_theme - загружает сохранённую тему (светлая/тёмная) из параметров URL
# get_theme_colors - возвращает цвета для текущей темы (фон, текст, акцент)
# get_css - генерирует CSS код на основе выбранной темы
# theme_selector - рисует кнопки выбора темы в боковой панели

from utils.api import get_exchange_rates
# get_exchange_rates - получает курсы валют (USD, EUR, RUB, CNY) с внешнего API

from utils.data_manager import init_data
# init_data - инициализирует все данные в session_state (ресурсы, теги, записи потребления)

import pages.home as home
# home - модуль с главной страницей

import pages.settings as settings
# settings - модуль со страницей настроек (справочник ТЭР и теги)

import pages.analytics as analytics
# analytics - модуль со страницей аналитики (графики потребления)

import pages.weather as weather
# weather - модуль со страницей погоды

import pages.calendar as calendar
# calendar - модуль со страницей календаря событий

import pages.support as support
# support - модуль со страницей поддержки и FAQ


# ============================================================================
# НАСТРОЙКА ВНЕШНЕГО ВИДА СТРАНИЦЫ (выполняется 1 раз при запуске)
# ============================================================================

st.set_page_config(
    page_title="SmartEnergyPro",  # Текст на вкладке браузера
    page_icon="⚡",               # Иконка на вкладке браузера (символ молнии)
    layout="wide"                # Широкий режим: контент растягивается на всю ширину
)


# ============================================================================
# ЗАГРУЗКА ДАННЫХ И ПРИМЕНЕНИЕ СТИЛЕЙ (выполняется 1 раз при запуске)
# ============================================================================

init_theme()    # Вызываем функцию: определяем, какая тема выбрана (светлая/тёмная/системная)
init_data()     # Вызываем функцию: создаём в памяти переменные с ресурсами, тегами и записями

colors = get_theme_colors()  # Получаем словарь с цветами: {bg, text, accent, border и т.д.}

st.markdown(get_css(colors), unsafe_allow_html=True)
# get_css(colors) - генерирует строку с CSS-правилами
# st.markdown - выводит HTML/CSS код на страницу
# unsafe_allow_html=True - разрешаем вставлять HTML (по умолчанию выключено для безопасности)


# ============================================================================
# БОКОВАЯ ПАНЕЛЬ НАВИГАЦИИ (SIDEBAR)
# ============================================================================
# with st.sidebar: - всё, что внутри этого блока, отображается в левой колонке
# Эта панель видна на всех страницах приложения

with st.sidebar:
    
    # --- ЗАГОЛОВОК ПАНЕЛИ ---
    st.markdown("## ⚡ SmartEnergyPro")
    # ## - заголовок второго уровня (h2)
    # markdown - форматирование текста с поддержкой Markdown-синтаксиса
    
    st.markdown("---")
    # --- - это горизонтальная линия-разделитель в Markdown (тег <hr>)
    
    # --- КНОПКИ ВЫБОРА ТЕМЫ ---
    theme_selector()
    # Эта функция рисует три кнопки: "🌞 Светлая", "🌙 Тёмная", "💻 Системная"
    # При нажатии на кнопку страница перезагружается с новой темой
    
    st.markdown("---")
    
    # --- КУРСЫ ВАЛЮТ ---
    st.markdown("**Курсы валют → ₸**")
    # **жирный текст** - жирное начертание в Markdown
    
    rates = get_exchange_rates()
    # rates - словарь вида: {"USD": 459.47, "EUR": 538.89, "RUB": 6.13, "CNY": 67.23}
    # Данные приходят с сайта exchangerate-api.com
    
    col1, col2 = st.columns(2)
    # Делим горизонтальное пространство на 2 равные колонки
    # col1 и col2 - это контейнеры, в которые можно складывать виджеты
    
    with col1:  # ЛЕВАЯ КОЛОНКА
        st.markdown(f"**$1 USD**  \n{rates['USD']} ₸")
        # f-строка: подставляет значение переменной rates['USD']
        # \n - перенос строки (разрыв строки в Markdown)
        st.markdown(f"**€1 EUR**  \n{rates['EUR']} ₸")
    
    with col2:  # ПРАВАЯ КОЛОНКА
        st.markdown(f"**₽1 RUB**  \n{rates['RUB']} ₸")
        st.markdown(f"**¥1 CNY**  \n{rates['CNY']} ₸")
    
    st.markdown("---")
    
    # --- НАВИГАЦИОННОЕ МЕНЮ (ГЛАВНЫЕ КНОПКИ) ---
    # st.radio - создаёт группу радиокнопок (можно выбрать только один пункт)
    page = st.radio(
        "📋 Меню",  # Заголовок группы (отображается над кнопками)
        [              # Список доступных страниц (6 штук):
            "🏠 Главная",    # индекс 0
            "📊 Аналитика",  # индекс 1
            "🌤️ Погода",     # индекс 2
            "📅 Календарь",  # индекс 3
            "⚙️ Настройки",  # индекс 4
            "🆘 Поддержка"   # индекс 5
        ],
        label_visibility="collapsed"  # Скрываем стандартный заголовок (оставляем только эмодзи 📋)
    )
    # st.radio возвращает ТЕКСТ выбранного пункта (например, "🏠 Главная")
    # Этот текст сохраняется в переменную page и используется ниже для навигации
    
    st.markdown("---")
    
    # --- ВЕРСИЯ ПРОГРАММЫ В САМОМ НИЗУ ПАНЕЛИ ---
    # Это HTML-код, вставленный через markdown
    # <p> - параграф (блок текста)
    # style='...' - CSS-стили внутри тега
    #   text-align: center - выравнивание по центру
    #   color: gray - серый цвет текста
    #   font-size: 12px - размер шрифта 12 пикселей
    st.markdown(
        "<p style='text-align: center; color: gray; font-size: 12px;'>Версия 3.4.2 | SmartEnergyPro © 2026</p>",
        unsafe_allow_html=True  # БЕЗ ЭТОГО HTML-теги НЕ БУДУТ РАБОТАТЬ! Streamlit экранирует HTML по умолчанию
    )


# ============================================================================
# НАВИГАЦИЯ ПО СТРАНИЦАМ (ОТОБРАЖЕНИЕ ВЫБРАННОГО КОНТЕНТА)
# ============================================================================
# Проверяем, какую страницу выбрал пользователь, и показываем соответствующую

if page == "🏠 Главная":
    # Если выбрана "🏠 Главная" - вызываем функцию show() из модуля home
    home.show()  # home.show() отрисовывает метрики и карточки с погодой

elif page == "📊 Аналитика":
    # Если выбрана "📊 Аналитика" - показываем страницу с графиками
    analytics.show()  # analytics.show() рисует графики потребления, CO₂, стоимости

elif page == "🌤️ Погода":
    # Если выбрана "🌤️ Погода" - показываем текущую температуру и ручной ввод замеров
    weather.show()  # weather.show() отображает погоду и журнал замеров

elif page == "📅 Календарь":
    # Если выбран "📅 Календарь" - показываем календарь с событиями
    calendar.show()  # calendar.show() рисует календарную сетку и список событий

elif page == "⚙️ Настройки":
    # Если выбраны "⚙️ Настройки" - показываем редактор ресурсов и тегов
    settings.show()  # settings.show() отображает таблицу с ресурсами ТЭР и поле для добавления тегов

elif page == "🆘 Поддержка":
    # Если выбрана "🆘 Поддержка" - показываем FAQ и форму обращений
    support.show()  # support.show() отображает часто задаваемые вопросы и контакты

