# =====================================================
# ПОДКЛЮЧАЕМ НЕОБХОДИМЫЕ БИБЛИОТЕКИ И МОДУЛИ
# =====================================================

import streamlit as st  # Главная библиотека для веб-приложения
from utils.theme import init_theme, get_theme_colors, get_css, theme_selector  # Функции для смены темы (светлая/тёмная/системная)
from utils.api import get_exchange_rates  # Функция для получения курсов валют (USD, EUR, RUB, CNY)
from utils.data_manager import init_data  # Функция для инициализации данных (ресурсы, теги, записи потребления)
import pages.home as home  # Страница "Главная" с метриками и погодой
import pages.settings as settings  # Страница "Настройки" для редактирования ресурсов и тегов
import pages.analytics as analytics  # Страница "Аналитика" с графиками потребления
import pages.weather as weather  # Страница "Погода" с текущей температурой и ручными замерами
import pages.calendar as calendar  # Страница "Календарь" для планирования событий (поверки, ТО)
import pages.support as support  # Страница "Поддержка" с FAQ и формой обращений


# =====================================================
# НАСТРОЙКА ГЛАВНОГО ОКНА ПРИЛОЖЕНИЯ
# =====================================================

st.set_page_config(
    page_title="SmartEnergyPro",  # Название, которое отображается на вкладке браузера
    page_icon="⚡",               # Иконка на вкладке браузера (молния)
    layout="wide"                # Широкий режим отображения (контент на всю ширину экрана)
)


# =====================================================
# ЗАГРУЗКА ТЕМЫ И ДАННЫХ ПРИ ЗАПУСКЕ ПРИЛОЖЕНИЯ
# =====================================================

init_theme()    # Загружаем сохранённую тему оформления (светлая/тёмная/системная) из параметров URL
init_data()     # Загружаем все данные в session_state (ресурсы ТЭР, теги, записи потребления, события календаря)
colors = get_theme_colors()  # Получаем объект с цветами для текущей темы (фон, текст, границы, акценты)
st.markdown(get_css(colors), unsafe_allow_html=True)  # Применяем CSS-стили к странице (цвета фона, кнопок, карточек)


# =====================================================
# БОКОВАЯ ПАНЕЛЬ (SIDEBAR) - ЭТО ЛЕВОЕ МЕНЮ НАВИГАЦИИ
# =====================================================
# Всё, что находится внутри блока with st.sidebar, отображается в левой части экрана

with st.sidebar:
    
    # ----- ЗАГОЛОВОК ПАНЕЛИ -----
    st.markdown("## ⚡ SmartEnergyPro")  # Жирный заголовок уровня h2 с иконкой молнии
    st.markdown("---")  # Горизонтальная линия-разделитель (тег <hr>)
    
    # ----- БЛОК ВЫБОРА ТЕМЫ ОФОРМЛЕНИЯ -----
    # theme_selector() рисует три кнопки: Светлая, Тёмная, Системная
    # При нажатии на кнопку тема меняется и страница перезагружается
    theme_selector()
    st.markdown("---")  # Разделитель
    
    # ----- БЛОК КУРСОВ ВАЛЮТ -----
    st.markdown("**Курсы валют → ₸**")  # Жирный текст "Курсы валют"
    rates = get_exchange_rates()  # Получаем словарь с курсами: {"USD": 459.47, "EUR": 538.89, ...}
    
    col1, col2 = st.columns(2)  # Делим горизонтальное пространство на две равные колонки
    
    with col1:  # Левая колонка
        st.markdown(f"**$1 USD**  \n{rates['USD']} ₸")  # Строка: $1 USD (перенос строки) 459.47 ₸
        st.markdown(f"**€1 EUR**  \n{rates['EUR']} ₸")  # Строка: €1 EUR (перенос строки) 538.89 ₸
    
    with col2:  # Правая колонка
        st.markdown(f"**₽1 RUB**  \n{rates['RUB']} ₸")  # Строка: ₽1 RUB (перенос строки) 6.13 ₸
        st.markdown(f"**¥1 CNY**  \n{rates['CNY']} ₸")  # Строка: ¥1 CNY (перенос строки) 67.23 ₸
    
    st.markdown("---")  # Разделитель
    
    # ----- НАВИГАЦИОННОЕ МЕНЮ (КНОПКИ ПЕРЕКЛЮЧЕНИЯ СТРАНИЦ) -----
    # st.radio создаёт группу кнопок-переключателей (как радиокнопки)
    # Только один пункт может быть выбран одновременно
    page = st.radio(
        "📋 Меню",  # Заголовок группы (виден пользователю)
        [                      # Список доступных страниц:
            "🏠 Главная",       # 0 - Главная страница с метриками
            "📊 Аналитика",     # 1 - Аналитика с графиками
            "🌤️ Погода",        # 2 - Погода и ручные замеры
            "📅 Календарь",     # 3 - Календарь событий
            "⚙️ Настройки",     # 4 - Настройки ресурсов и тегов
            "🆘 Поддержка"      # 5 - Поддержка и FAQ
        ],
        label_visibility="collapsed"  # Скрываем стандартный заголовок (оставляем только эмодзи)
    )
    
    st.markdown("---")  # Разделитель перед информационной частью
    
    # ----- ИНФОРМАЦИОННЫЙ БЛОК (ВЕРСИЯ И КОПИРАЙТ) -----
    # st.caption выводит текст мелким шрифтом (как подпись)
    st.caption("v.3.4.2 (Stable Edition)")  # Номер версии программы
    st.caption("© 2026 SmartEnergyPro")     # Копирайт (авторские права)
    
    # ----- ВЕРСИЯ ПРОГРАММЫ В САМОМ НИЗУ ПАНЕЛИ -----
    # Это HTML-код, который мы вставляем через markdown
    # Параметры стиля: text-align center - по центру, color gray - серый цвет, font-size 12px - размер шрифта
    st.markdown(
        "<p style='text-align: center; color: gray; font-size: 12px;'>Версия 3.4.2 | SmartEnergyPro © 2026</p>",
        unsafe_allow_html=True  # Разрешаем вставку HTML-кода (встроенные теги)
    )
    # Эта строка отображается после всего остального контента в боковой панели (в самом низу)


# =====================================================
# НАВИГАЦИЯ ПО СТРАНИЦАМ (ОТОБРАЖЕНИЕ ВЫБРАННОГО КОНТЕНТА)
# =====================================================
# Здесь мы проверяем, какой пункт меню выбрал пользователь,
# и вызываем соответствующую функцию show() из нужного модуля

if page == "🏠 Главная":
    home.show()          # Показываем главную страницу (метрики, карточки, погода по городам)

elif page == "📊 Аналитика":
    analytics.show()     # Показываем страницу аналитики (графики потребления, т.у.т., CO₂, стоимость)

elif page == "🌤️ Погода":
    weather.show()       # Показываем страницу погоды (текущая температура + ручной журнал замеров)

elif page == "📅 Календарь":
    calendar.show()      # Показываем календарь (события, поверки, ТО, отчётность)

elif page == "⚙️ Настройки":
    settings.show()      # Показываем страницу настроек (управление ресурсами ТЭР и тегами)

elif page == "🆘 Поддержка":
    support.show()       # Показываем страницу поддержки (FAQ, контакты, форма обращений)


# =====================================================
# КОНЕЦ ФАЙЛА
# =====================================================
